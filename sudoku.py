def sudoku(board):
    row = {}
    col = {}
    for i in range(10):
        row[i] = []
        col[i] = []
    i = 0
    while i < 9:
        j = 0
        while j < 9:
            quad = board[i][j:j+3]+board[i+1][j:j+3]+board[i+2][j:j+3]
            for k in range(9):
                if quad[k] != '.':
                    for l in range(9):
                        if k == l:
                            continue
                        else:
                            if quad[k] == quad[l]:
                                return False
            j += 3

        i += 3

    i = 0
    while i < 9:
        j = 0
        while j < 9:
            if board[i][j] != '.':
                if board[i][j] in row[i]:
                    return False
                elif board[i][j] in col[j]:
                    return False
                else:
                    row[i].append(board[i][j])
                    col[j].append(board[i][j])
            j += 1
        i += 1
    print(row)
    print(col)

    return True


